import UIKit

extension UIColor {
    
    typealias Color = UIColor.Bitrise
    
    enum Bitrise {
        static let purple = UIColor(red:0.28, green:0.18, blue:0.35, alpha:1.00)
        static let green = UIColor(red:0.18, green:0.77, blue:0.65, alpha:1.00)
    }
}
